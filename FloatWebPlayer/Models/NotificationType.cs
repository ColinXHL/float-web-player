namespace FloatWebPlayer.Models;

/// <summary>
/// 通知类型枚举，定义不同类型的通知及其视觉样式
/// </summary>
public enum NotificationType
{
    /// <summary>
    /// 信息提示（蓝色）
    /// </summary>
    Info,

    /// <summary>
    /// 成功提示（绿色）
    /// </summary>
    Success,

    /// <summary>
    /// 警告提示（橙色）
    /// </summary>
    Warning,

    /// <summary>
    /// 错误提示（红色）
    /// </summary>
    Error
}
